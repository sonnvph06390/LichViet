package com.thuanthanh.lichviet.model;

public class TuVi {
    private int imgTuVi;
    private String tvTuVi;

    public TuVi(int imgTuVi, String tvTuVi) {
        this.imgTuVi = imgTuVi;
        this.tvTuVi = tvTuVi;
    }

    public int getImgTuVi() {
        return imgTuVi;
    }

    public void setImgTuVi(int imgTuVi) {
        this.imgTuVi = imgTuVi;
    }

    public String getTvTuVi() {
        return tvTuVi;
    }

    public void setTvTuVi(String tvTuVi) {
        this.tvTuVi = tvTuVi;
    }
}
